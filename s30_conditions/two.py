""" skapa en funktion som tar två argument
 som returnerar om första argumentet är mindre än andra argument
namnet på funktionen ska vara:
första_mindre_än_andra
"""

def första_mindre_än_andra(arg1,arg2):
    if arg1 < arg2:
        return "arg1 < arg2"
    else:
        return "NONE"



print(första_mindre_än_andra(5, 2))

